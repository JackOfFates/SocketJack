        private static byte[] GetRawBytes(object obj) {
            if (obj == null) return null;
            if (obj is byte[] ba) return ba;
            if (obj is List<byte> lb) return lb.ToArray();
            return null;
        }

        #endregion

        private void Log(string text) {
            LogOutput?.Invoke(text);
        }
    }

    /// <summary>
    /// Payload returned by the <c>/stream/active</c> endpoint with the current broadcast status.
    /// </summary>
    public class MyPayload {
        /// <summary>Whether any publish source (RTMP or OBS upload) is currently active.</summary>
        public bool Active { get; set; }
        /// <summary>The number of viewers currently connected.</summary>
        public int Viewers { get; set; }
        /// <summary>Whether an RTMP publish session is active.</summary>
        public bool Rtmp { get; set; }
        /// <summary>Whether an OBS upload connection is active.</summary>
        public bool Upload { get; set; }
    }

    /// <summary>
    /// Snapshot of RTMP stream statistics returned by <see cref="BroadcastServer.UpdateStats"/>.
    /// </summary>
    public class RtmpStats {
        /// <summary>Whether an RTMP session is currently active.</summary>
        public bool Active { get; set; }
        /// <summary>The RTMP app name.</summary>
        public string App { get; set; } = "";
        /// <summary>The RTMP stream key.</summary>
        public string StreamKey { get; set; } = "";
        /// <summary>Total video frames received.</summary>
        public long VideoFrames { get; set; }
        /// <summary>Total audio frames received.</summary>
        public long AudioFrames { get; set; }
        /// <summary>Total dropped frames.</summary>
        public long DroppedFrames { get; set; }
        /// <summary>Total bytes received.</summary>
        public long TotalBytes { get; set; }
        /// <summary>Current bitrate in kilobits per second.</summary>
        public double BitrateKbps { get; set; }
    }
